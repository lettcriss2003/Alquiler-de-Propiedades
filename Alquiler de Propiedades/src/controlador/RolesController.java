package controlador;

import controlador.listas.Exepciones.ListaVaciaException;
import controlador.listas.Exepciones.PosicionNoEncontradaException;
import controlador.listas.ListaEnlazada;
import modelo.Rol;

/**
 *
 * @author leomah
 */
public class RolesController {
    private ListaEnlazada<Rol> listaRoles;

    public ListaEnlazada<Rol> getListaRoles() {
        if (this.listaRoles == null) {
            this.listaRoles = new ListaEnlazada<>();
        }
        return listaRoles;
    }

    public void setListaRoles(ListaEnlazada<Rol> listaRoles) {
        this.listaRoles = listaRoles;
    }
    
    public void agregarRol(Rol r){
        getListaRoles().insertar(r);
    }
    
    public Rol obtenerRol(Integer pos) throws ListaVaciaException, PosicionNoEncontradaException{
        return getListaRoles().obtener(pos);
    }
    
    public Integer generarID(){
        return getListaRoles().getTamanio() + 1;
    }
    
    public void eliminarRol(Integer pos) throws ListaVaciaException, PosicionNoEncontradaException{
        getListaRoles().eliminarPosicion(pos);
        for (int i = 0; i < getListaRoles().getTamanio(); i++) {
            Rol r = obtenerRol(i);
            r.setId(i+1);
            modificarPosicion(r, i);
        }
    }
    
    public void modificarPosicion(Rol r, Integer pos){
        getListaRoles().modificarPoscicion(r, pos);
    }
    
}
